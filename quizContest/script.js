topic.forEach((e)=>{
    e.addEventListener("click",selectedTopics);
})
function selectedTopics(e){
    let element = e.currentTarget.children;
    if(!topics[element[0].innerText]){
        topics[element[0].innerText]=1;
        element[0].style.background="#FCC822";
        element[1].style.display="flex";
        return;
    }
    delete topics[element[0].innerText];
    element[0].style.background="grey";
    element[1].style.display="none";
}


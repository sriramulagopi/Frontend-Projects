let questions1;
btn3.addEventListener("click",startQuiz);
function startQuiz(){
    let array = Object.keys(topics)
    if(array.length>=3){
        main.style.display="none";
        sec4.style.display="flex";
        let array2 = [];
        quizData.forEach((item)=>{
            if(array.includes(item.category)){
                array2.push(...item.questions);
            }
        })
        questions1=array2;
        enteredInto();
    }
    else{
        alert("Atleast you have to select 3 topics");
    }
}
let index = 0;
let answers = [];
function enteredInto(){
    ques.innerText=questions1[index].question;
    for (let i=0;i<4;i++){
        label[i].innerText=questions1[index].options[i];
        input[i].value=questions1[index].options[i];
    }
    if(answers[index]){
        form.option.value=answers[index];
    }
}
function next(){
    if(questions1.length-1===index){
        return;
    }
    answers[index]=form.option.value;
    form.reset();
    index++;
    enteredInto();

}
function previ(){
    if(index===0){
        return
    }
    index--;
    enteredInto();
}